    private void btbBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btbBuscarActionPerformed
    String valortxt = txtBuscar.getText();
    if (!valortxt.isEmpty() && valortxt.matches("\\d+")) {
        int valor = Integer.parseInt(valortxt);
        Nodo no = l.buscarNodo(valor);
        if (no != null) {
            // El nodo fue encontrado, realiza la acción deseada
            JOptionPane.showMessageDialog(null,"Encontrado : " +no.dato); //Lo hacia con esto pero mas practico es la linea siguiente
            txtAlista.append("\nValor encontrado : "+valortxt);
        } else {
            // El nodo no fue encontrado
            JOptionPane.showMessageDialog(null, "Valor no encontrado :(");
        }
    } else {
        // El valor ingresado no es un número válido
        JOptionPane.showMessageDialog(null, "Debe ingresar un numero a buscar");
    }
    txtBuscar.setText("");
    }//GEN-LAST:event_btbBuscarActionPerformed

