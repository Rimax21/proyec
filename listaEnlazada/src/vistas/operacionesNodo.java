    private void txtEliminarKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtEliminarKeyTyped
        char c = evt.getKeyChar();
        String txt = txtEliminar.getText();
            if(!(Character.isDigit(c)) || txt.length() >= 3 || (txt.isEmpty() && c == '0')){
                evt.consume();
        }
    }//GEN-LAST:event_txtEliminarKeyTyped

    private void btbinsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btbinsertarActionPerformed
        String valortxt = txtInsertar.getText();
        if(!valortxt.isEmpty() && valortxt.matches("\\d+")){
        int valor = Integer.parseInt(valortxt);
        l.insertarNodoOrdenado(valor);
        JOptionPane.showMessageDialog(null, "Valor ingresado : "+valortxt);
        }else{
            JOptionPane.showMessageDialog(null, "No debe de estar vacio ingrese un nodo por favor");
        }
        txtInsertar.setText("");
    }//GEN-LAST:event_btbinsertarActionPerformed

