    private void btbEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btbEliminarActionPerformed
    boolean nodoEliminado = c.eliminarNodoCola();
    Nodo nodoFrente = c.cola.peek();
    int numeroEliminado = nodoFrente.dato;

    if (nodoEliminado) {
        // El nodo fue eliminado exitosamente
        txtAlista.append("\nEliminado: " +numeroEliminado);
    } else {
        // La cola está vacía
        JOptionPane.showMessageDialog(null, "La cola está vacía, no se puede eliminar");
    }
    }//GEN-LAST:event_btbEliminarActionPerformed

