    private void txtModificarNuevoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtModificarNuevoKeyTyped
        // TODO add your handling code here:
        char c = evt.getKeyChar();
        String txt = txtModificarNuevo.getText();
        if (!(Character.isDigit(c)) || txt.length() >= 3 || (txt.isEmpty() && c == '0')) {
            evt.consume();
        }
    }//GEN-LAST:event_txtModificarNuevoKeyTyped

