    private void btbModifcarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btbModifcarActionPerformed
        String valortxt = txtModificarNuevo.getText();
        String valorsito = txtModificarAntiguo.getText();

        if (!valortxt.isEmpty() && valortxt.matches("\\d+")) {
            int valorBuscado = Integer.parseInt(valortxt);
            String nuevoValorTxt = txtModificarAntiguo.getText();

            if (!nuevoValorTxt.isEmpty() && nuevoValorTxt.matches("\\d+")) {
                int nuevoValor = Integer.parseInt(nuevoValorTxt);

                // Call the modificarNodo method and check if the node was modified
                boolean nodoModificado = c.modificarNodoCola(valorBuscado, nuevoValor);
                if (nodoModificado) {
                    // The node was found and modified, append the modification message to txtAlista
                    JOptionPane.showMessageDialog(null, "Nodo modificado " + valorBuscado + " --> " + valorsito);
                    txtAlista.append("\nValor : " + valortxt + " Modificado por : " + valorsito);
                } else {
                    // The node was not found
                    JOptionPane.showMessageDialog(null, "Nodo no encontrado");
                }
            } else {
                // The new value entered is not a valid number
                JOptionPane.showMessageDialog(null, "Se debe ingresar un número");
            }
        }

        txtModificarNuevo.setText("");
        txtModificarAntiguo.setText("");
    }//GEN-LAST:event_btbModifcarActionPerformed

